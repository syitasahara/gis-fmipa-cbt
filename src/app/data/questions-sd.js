// Function to randomize questions
export const randomizeQuestions = (questions, seed = null) => {
  if (!Array.isArray(questions)) return [];
  
  const shuffled = [...questions];
  const random = seed ? seededRandom(seed) : Math.random;
  
  // Fisher-Yates shuffle algorithm
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  
  return shuffled;
};

// Seeded random function for consistent randomization
function seededRandom(seed) {
  let m = 0x80000000; // 2**31
  let a = 1103515245;
  let c = 12345;
  
  seed = seed || Date.now();
  return function() {
    seed = (a * seed + c) % m;
    return seed / (m - 1);
  };
}

// Function to randomize answers within questions
export const randomizeAnswers = (questions, seed = null) => {
  return questions.map((question, questionIndex) => {
    if (!question.answers || !Array.isArray(question.answers)) {
      return question;
    }
    
    const questionSeed = seed ? seed + questionIndex : null;
    const shuffledAnswers = randomizeQuestions(question.answers, questionSeed);
    
    return {
      ...question,
      answers: shuffledAnswers,
      opsi: shuffledAnswers.map(answer => 
        answer.answer_text || answer.answer_img || `Opsi ${shuffledAnswers.indexOf(answer) + 1}`
      )
    };
  });
};
